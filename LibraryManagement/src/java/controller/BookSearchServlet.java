    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("search.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
          String criteria=request.getParameter("sCriteria");
          String value=request.getParameter("sValue");
          ArrayList<Book> books=null; 
          switch(criteria){
              case "bid": books=Library.search(BookDB.getAllBook(),s->s.getBookID().equals(value)); break;
              case "tittle": books=Library.search(BookDB.getAllBook(),s->s.getTitle().contains(value)); break;
              case "author": books=Library.search(BookDB.getAllBook(),s->s.getTacgia().contains(value));break;
              case "yearpub":books= Library.search(BookDB.getAllBook(),s->s.getPubYear()==Integer.parseInt(value)); break;
              case "cat":books= Library.search(BookDB.getAllBook(),s->s.getCategory().equals(value)); break;
              default: books=BookDB.getAllBook();
          }
          request.getSession().setAttribute("list", books);
          request.getRequestDispatcher("listtag.jsp").forward(request, response);

    }

