    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("cal.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String side1 = request.getParameter("side1");
        String side2 = request.getParameter("side2");
        String side3 = request.getParameter("side3");
        Triangle model = new Triangle(Float.parseFloat(side1),
                Float.parseFloat(side2), Float.parseFloat(side3));
        int check = model.validation();
        request.setAttribute("RS", check);
        request.setAttribute("RS1", model.calParameter());
        request.setAttribute("RS2", model.calArea());
        request.getRequestDispatcher("result.jsp").include(request, response);
    }

